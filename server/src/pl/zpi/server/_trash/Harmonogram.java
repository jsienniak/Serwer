    int g_stop;
    int dni;
    int m_id;
    int p;
    String w_start;
    String w_end;
    int active;
	public int getG_start() {
		return g_start;
	}
	public void setG_start(int g_start) {
		this.g_start = g_start;
	}
	public int getG_stop() {
		return g_stop;
	}
	public void setG_stop(int g_stop) {
		this.g_stop = g_stop;
	}
	public int getDni() {
		return dni;
	}
	public void setDni(int dni) {
		this.dni = dni;
	}
	public int getM_id() {
		return m_id;
	}
	public void setM_id(int m_id) {
		this.m_id = m_id;
	}
	public int getP() {
		return p;
	}
	public void setP(int p) {
		this.p = p;
	}
	public String getW_start() {
		return w_start;
	}
	public void setW_start(String w_start) {
		this.w_start = w_start;
	}
